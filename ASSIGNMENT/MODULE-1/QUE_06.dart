// 6. Write a program to find the simple Interest.

void main()
{
  // Simple interest is calculated with the following formula: S.I. = (P × R × T)/100,
  // where P = Principal, R = Rate of Interest in % per annum, and T = Time
  int p = 50,r = 5,t=6;

  double simple_interest = (p*r*t)/100;

  print("Simple interest is calculated is $simple_interest");

}